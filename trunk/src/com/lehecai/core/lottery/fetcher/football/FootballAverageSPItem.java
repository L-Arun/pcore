package com.lehecai.core.lottery.fetcher.football;

/**
 * 存储抓取的足彩的平均SP指数
 * @author 唐容
 *
 */
public class FootballAverageSPItem {

	private String phase; // 期号
	
	private int matchIndex; // 场次顺序
	
	private String averageSP_S;		//平均指数-胜
	
	private String averageSP_P;		//平均指数-平
	
	private String averageSP_F;		//平均指数-负
	
	private String averageSP_BC_S;	//平均指数-半场-胜
	
	private String averageSP_BC_P;	//平均指数-半场-平
	
	private String averageSP_BC_F;	//平均指数-半场-负

	public String getPhase() {
		return phase;
	}

	public void setPhase(String phase) {
		this.phase = phase;
	}

	public int getMatchIndex() {
		return matchIndex;
	}

	public void setMatchIndex(int matchIndex) {
		this.matchIndex = matchIndex;
	}

	public String getAverageSP_S() {
		return averageSP_S;
	}

	public void setAverageSP_S(String averageSPS) {
		averageSP_S = averageSPS;
	}

	public String getAverageSP_P() {
		return averageSP_P;
	}

	public void setAverageSP_P(String averageSPP) {
		averageSP_P = averageSPP;
	}

	public String getAverageSP_F() {
		return averageSP_F;
	}

	public void setAverageSP_F(String averageSPF) {
		averageSP_F = averageSPF;
	}

	public String getAverageSP_BC_S() {
		return averageSP_BC_S;
	}

	public void setAverageSP_BC_S(String averageSPBCS) {
		averageSP_BC_S = averageSPBCS;
	}

	public String getAverageSP_BC_P() {
		return averageSP_BC_P;
	}

	public void setAverageSP_BC_P(String averageSPBCP) {
		averageSP_BC_P = averageSPBCP;
	}

	public String getAverageSP_BC_F() {
		return averageSP_BC_F;
	}

	public void setAverageSP_BC_F(String averageSPBCF) {
		averageSP_BC_F = averageSPBCF;
	}
	
}
