package com.lehecai.core.exception;

/**
 * 不支持的彩票配置异常
 * 如开奖结果未配置,开奖详情未配置
 * @author leiming
 *
 */
public class UnsupportedLotteryConfigException extends Exception {

	private static final long serialVersionUID = 4174889441254987869L;
	public UnsupportedLotteryConfigException(String message) {
		super(message);
	}
	
	public UnsupportedLotteryConfigException() {
		this("未找到对应彩票配置");
	}
}
